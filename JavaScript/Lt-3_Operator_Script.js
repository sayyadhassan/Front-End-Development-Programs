const a = 2;
const b = 3;
 
// Arithmetic Operator : Exponential Operatro,(2**3) -> 2 to the power 3 = 8
const z = a ** b;
console.log(z);

const c = 6;   // it is Number
const d = '6'; // it is String

// Comparesion Operator : Equal to operator, return true if the operand/values are equal
const f = (c == d);
console.log(f);    // will print true, compare the values only instead of data type

// Comparesion Operator : Strictly Equal to operator, return true if the values are equal and operand is of same type
const g= (c === d);  
console.log(g);    // will print false, compare values along with data type



